-- What is the daily average of sales, grouped by sales territory?

SELECT
  dd.FULLDATEALTERNATEKEY AS SaleDate,
  dst.SALESTERRITORYREGION AS SalesTerritory,
  AVG(fis.SALESAMOUNT) AS DailyAverageSales
FROM
  KATER.ADVENTUREWORKSDW.FACTINTERNETSALES fis
  LEFT JOIN KATER.ADVENTUREWORKSDW.DIMDATE dd ON fis.ORDERDATEKEY = dd.DATEKEY
  LEFT JOIN KATER.ADVENTUREWORKSDW.DIMSALESTERRITORY dst ON fis.SALESTERRITORYKEY = dst.SALESTERRITORYKEY
GROUP BY
  dd.FULLDATEALTERNATEKEY,
  dst.SALESTERRITORYREGION
ORDER BY
  dd.FULLDATEALTERNATEKEY,
  dst.SALESTERRITORYREGION
LIMIT 300;