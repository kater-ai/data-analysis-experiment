-- What is our inventory turnover?

SELECT
  P.PRODUCTKEY,
  P.ENGLISHPRODUCTNAME AS PRODUCTNAME,
  SUM(F.ORDERQUANTITY) / COUNT(DISTINCT F.SHIPDATEKEY) AS Inventory_Turnover
FROM
  KATER.ADVENTUREWORKSDW.FACTINTERNETSALES F
  JOIN KATER.ADVENTUREWORKSDW.DIMPRODUCT P ON F.PRODUCTKEY = P.PRODUCTKEY
GROUP BY
  P.PRODUCTKEY,
  P.ENGLISHPRODUCTNAME;